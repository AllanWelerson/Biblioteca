    private void jtEmpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtEmpMouseClicked
        
        if(jtEmp.getSelectedRow() != -1){
            
            txtCodEmp.setText(jtEmp.getValueAt(jtEmp.getSelectedRow(), 0).toString());
            txtDataInicio.setText(jtEmp.getValueAt(jtEmp.getSelectedRow(), 1).toString());
            txtDataFim.setText(jtEmp.getValueAt(jtEmp.getSelectedRow(), 2).toString());
            txtStatusEmp.setText(jtEmp.getValueAt(jtEmp.getSelectedRow(), 5).toString());
            
            Livro l = new Livro();
            LivroDAO ldao = new LivroDAO();
            l = (Livro) jtEmp.getValueAt(jtEmp.getSelectedRow(), 3);
            txtCodLivro.setText(l.getCodigo());
            txtDescLivro.setText(l.getDescricao());
            txtPagLivros.setText(String.valueOf(l.getPag()).toString());
            txtSetorLivro.setText(String.valueOf(l.getSetor()).toString() );
            
            
            Cliente c = new Cliente();
            ClienteDAO cdao = new ClienteDAO();
            
            c = (Cliente) jtEmp.getValueAt(jtEmp.getSelectedRow(),4);
            txtNomeCliente.setText(c.getNome());
            txtCpfCliente.setText(c.getCpf());
            txtFoneCliente.setText(c.getTelefone());
            txtStatusCliente.setText(c.getStatus());
            
        }
        
    }//GEN-LAST:event_jtEmpMouseClicked

