    public void readTableForDesc(String desc){
        
        DefaultTableModel model = (DefaultTableModel) jtSetor.getModel();
        model.setNumRows(0);
        
        SetorDAO dao = new SetorDAO();
        
        for(Setor s: dao.readForDesc(desc)){
            model.addRow(new Object[]{s.getId(),s.getDescricao()});
        }
        
    }
    
    public void readTable(){
        
        DefaultTableModel model = (DefaultTableModel) jtSetor.getModel();
        model.setNumRows(0);
        
        SetorDAO dao = new SetorDAO();
        
        for(Setor s: dao.read() ){
            model.addRow(new Object[]{s.getId(),s.getDescricao()});
        }
        
    }
    
    
