    public void save(){
        
        if(!txtDescricao.getText().equals("")){
            
            Setor setor = new Setor();
            setor.setId(Integer.parseInt(txtId.getText()));
            setor.setDescricao(txtDescricao.getText());
            
            SetorDAO dao = new SetorDAO();
            
            if(dao.update(setor)){
                JOptionPane.showMessageDialog(null, "Alterado com sucesso!", "Cadastro!", JOptionPane.PLAIN_MESSAGE);
                readTable();
                
            }else{
               JOptionPane.showMessageDialog(null, "Erro ao Atualizar","Atualizar Cliente",JOptionPane.ERROR_MESSAGE); 
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Digite o texto","Atualizar Cliente",JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    public void moveDates(){
        if(jtSetor.getSelectedRow() != -1){
            txtId.setText(jtSetor.getValueAt(jtSetor.getSelectedRow(),0).toString());
            txtDescricao.setText(jtSetor.getValueAt(jtSetor.getSelectedRow(),1).toString());
        }
    }
    
